package Practice;

public class ArithmeticShortcuts {
    public static void main(String[] args) {
        /* Arithmetic Shortcuts
        Postfix: x++ is the same as x=x+1
                 x-- is the same as x=x-1
        Prefix: ++x is the same as x=x+1
                --x is the same as x=x-1
         */

        int a=0;
        System.out.println(a);

        a++; // a=a+1
        System.out.println(a);


    }
}
